/**
 * Cleanup helper that scans for any building card matching "E2E Building" or "IsolationTest"
 * and deletes them. Due to DB cascade constraints, this cleanly purges all cascade-related
 * rooms, tenants, payments, and expenses!
 */
export async function cleanupAllTestBuildings(page: Page) {
  await page.goto('/buildings');

  // Let the page load
  await page.waitForLoadState('networkidle');

  // Find all cards containing test markers
  const cards = page.locator('div.bg-white', {
    hasText: /(E2E Building|IsolationTest|TestBuilding|CRUDTest)/i
  });

  const count = await cards.count();
  for (let i = 0; i < count; i++) {
    // Refresh cards query to avoid stale element reference
    const activeCard = page.locator('div.bg-white', {
      hasText: /(E2E Building|IsolationTest|TestBuilding|CRUDTest)/i
    }).first();

    if (await activeCard.isVisible()) {
      // Open dropdown
      await activeCard.locator('button').first().click();
      
      // Click delete
      await page.getByRole('menuitem', { name: /delete/i }).click();
      
      // Confirm dialog
      await page.getByRole('button', { name: 'Confirm' }).click();
      
      // Wait for success toast
      await expect(page.getByText(/building deleted/i)).toBeVisible();
      
      // Tiny pause to let the UI react
      await page.waitForTimeout(500);
    }
  }
}
