export async function createRentPayment(
  page: Page,
  tenantName: string,
  amount: number,
  month: number = new Date().getMonth() + 1,
  year: number = new Date().getFullYear(),
  status: 'paid' | 'unpaid' | 'partial' = 'paid',
  paidDate: string = new Date().toISOString().split('T')[0]
) {
  await page.goto('/rent');
  await page.getByRole('button', { name: /record payment/i }).click();

  // Select Tenant
  await page.locator('label:has-text("Tenant *") + div button, label:has-text("Tenant *") + button').click();
  await page.getByRole('option', { name: new RegExp(tenantName, 'i') }).click();

  // Month
  await page.locator('label:has-text("Month") + div button, label:has-text("Month") + button').click();
  await page.getByRole('option', { name: new RegExp(MONTH_NAMES[month - 1], 'i') }).click();

  await page.getByLabel('Year').fill(String(year));
  await page.getByLabel('Amount (₹)').fill(String(amount));

  // Status
  await page.locator('label:has-text("Status") + div button, label:has-text("Status") + button').click();
  await page.getByRole('option', { name: new RegExp(status, 'i') }).click();

  if (status === 'paid') {
    await page.getByLabel('Paid Date').fill(paidDate);
  }

  await page.getByRole('button', { name: 'Record', exact: true }).click();
  await expect(page.getByText('Payment recorded!')).toBeVisible();
}

