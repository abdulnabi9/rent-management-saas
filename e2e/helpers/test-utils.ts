  await page.getByRole('option', { name: buildingName }).click();

  await page.getByLabel('Room Number *').fill(roomNumber);
  await page.getByLabel('Floor *').fill(String(floor));

  // Type selection
  await page.locator('label:has-text("Room Type *") + div button, label:has-text("Room Type *") + button').click();
  await page.getByRole('option', { name: new RegExp(type, 'i') }).click();

  await page.getByLabel('Capacity *').fill(String(capacity));
